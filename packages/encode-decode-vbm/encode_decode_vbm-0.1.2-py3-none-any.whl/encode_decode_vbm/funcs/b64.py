def preproc(mess):

    mess = ''.join([bin(ord(char))[2:].zfill(8) for char in mess])

    while (len(mess) % 6) != 0:
        mess += '0'

    mess = [int(mess[i:i+6], 2) for i in range(0, len(mess), 6)]

    return mess


